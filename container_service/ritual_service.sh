# –î–æ–¥–∞–≤–∞–Ω–Ω—è –≤ –∞–≤—Ç–æ–∑–∞–ø—É—Å–∫
echo "üîß –í–∫–ª—é—á–µ–Ω–∏–µ —Å–µ—Ä–≤–∏—Å–∞..."
